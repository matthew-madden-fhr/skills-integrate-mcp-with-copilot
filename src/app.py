"""
High School Management System API

A super simple FastAPI application that allows students to view and sign up
for extracurricular activities at Mergington High School.
"""

from fastapi import FastAPI, HTTPException
from fastapi.staticfiles import StaticFiles
from fastapi.responses import RedirectResponse
import os
import json
from pathlib import Path

app = FastAPI(title="Mergington High School API",
              description="API for viewing and signing up for extracurricular activities")

# Mount the static files directory
current_dir = Path(__file__).parent
app.mount("/static", StaticFiles(directory=os.path.join(Path(__file__).parent,
          "static")), name="static")

# Define path for JSON data file
DATA_FILE = os.path.join(current_dir, "data", "activities.json")

# Helper functions for JSON file operations
def load_activities():
    """Load activities data from JSON file if it exists, otherwise return default data"""
    # Make sure the data directory exists
    os.makedirs(os.path.dirname(DATA_FILE), exist_ok=True)
    
    try:
        if os.path.exists(DATA_FILE):
            with open(DATA_FILE, "r") as file:
                return json.load(file)
        else:
            # Return default data if file doesn't exist
            return default_activities
    except Exception as e:
        print(f"Error loading activities data: {str(e)}")
        return default_activities

def save_activities(data):
    """Save activities data to JSON file"""
    try:
        os.makedirs(os.path.dirname(DATA_FILE), exist_ok=True)
        with open(DATA_FILE, "w") as file:
            json.dump(data, file, indent=2)
    except Exception as e:
        print(f"Error saving activities data: {str(e)}")

